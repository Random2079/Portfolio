# sys нужен, чтобы программа могла нормально завершиться и общаться с системой
import sys

# Из библиотеки PyQt5 берём готовые детали для интерфейса
# QApplication — это сама программа-приложение (как пустой лист)
# QMainWindow — главное окно с рамкой, заголовком и кнопками «свернуть/закрыть»
# QPushButton — обычная кнопка, на которую можно нажимать
# QLabel — текстовая надпись (просто текст на экране)
from PyQt5.QtWidgets import QApplication, QMainWindow, QPushButton, QLabel

# Создаём свой класс окна на основе стандартного QMainWindow
class MyWindow(QMainWindow):
    def __init__(self):
        # Сначала делаем всё, что делает обычное окно (рамка, заголовок, кнопки «закрыть»)
        super().__init__()

        # Задаём заголовок окна (то, что будет написано в верхней синей полоске)
        self.setWindowTitle("Тестовое окно")

        # Задаём положение окна на экране и его размеры
        # Первые два числа — отступ от левого верхнего угла экрана (x, y)
        # Вторые два числа — ширина и высота окна в пикселях
        self.setGeometry(100, 100, 300, 200)

        # Создаём кнопку
        # "Нажми меня" — текст, который будет на кнопке
        # self — говорим, что кнопка принадлежит этому окну
        self.button = QPushButton("Нажми меня", self)

        # Размещаем кнопку внутри окна
        # 100 — отступ от левого края окна (в пикселях)
        # 50 — отступ от верхнего края окна (в пикселях)
        self.button.move(100, 50)

        # Самое важное: связываем нажатие кнопки с нашим действием
        # clicked — это сигнал, который кнопка отправляет, когда на неё нажимают
        # connect(self.on_click) — говорим: «Когда придёт сигнал clicked, вызови метод on_click»
        self.button.clicked.connect(self.on_click)

        # Создаём текстовую надпись
        # "Я текст" — текст, который будет показан при запуске
        self.label = QLabel("Я текст", self)

        # Размещаем надпись внутри окна (100 пикселей от левого края, 100 — от верхнего)
        self.label.move(100, 100)

    # Этот метод вызывается автоматически, когда кнопка нажата
    def on_click(self):
        # setText — меняем текст нашей надписи на новый
        self.label.setText("Кнопка нажата!")

# Точка входа: этот блок выполняется, только если файл запущен напрямую
if __name__ == '__main__':
    # Создаём само приложение (без этой строки ничего не заработает)
    # sys.argv — список аргументов командной строки (нам не нужен, но обязателен для QApplication)
    app = QApplication(sys.argv)

    # Создаём экземпляр нашего окна
    window = MyWindow()

    # Показываем окно на экране (без этой строки окно будет невидимым)
    window.show()

    # Запускаем бесконечный цикл обработки событий (ждём кликов, нажатий, закрытия окна)
    # sys.exit() — когда окно закроется, программа завершится и вернёт код 0 (всё ок)
    sys.exit(app.exec_())